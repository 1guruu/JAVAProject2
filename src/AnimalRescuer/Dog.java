package AnimalRescuer;

public class Dog {

    private String dogName;
    private String dogRace;
    private String dogColor;
    private int ageInMonths;
    private String dogFavoriteFood;
    private String dogFavouriteActivity;
    private String dogHealth;
    private String dogHunger;
    private String dogMood;

    public void setDogName(String dogName){
        this.dogName=dogName;
    }
    public String getDogName(){
        return dogName;
    }
    public void setDogRace(String dogRace){
        this.dogRace=dogRace;
    }
    public String getDogRace(){
        return dogRace;
    }
    public void setDogColor(String dogColor){
        this.dogColor=dogColor;
    }
    public String getDogColor(){
        return dogColor;
    }
    public void setAgeInMonths(int ageInMonths){
        this.ageInMonths=ageInMonths;
    }
    public int getAgeInMonths(){
        return ageInMonths;
    }
    public void setDogFavoriteFood(String dogFavoriteFood){
        this.dogFavoriteFood=dogFavoriteFood;
    }
    public String getDogFavoriteFood(){
        return dogFavoriteFood;
    }
    public void setDogFavouriteActivity(String dogFavouriteActivity){
        this.dogFavouriteActivity=dogFavouriteActivity;
    }
    public String getDogFavouriteActivity(){
        return dogFavouriteActivity;
    }
    public void setDogHealth(String dogHealth){
        this.dogHealth=dogHealth;
    }
    public String getDogHealth(){
        return dogHealth;
    }
    public void setDogHunger(String dogHunger){
        this.dogHunger=dogHunger;
    }
    public String getDogHunger(){
        return dogHunger;
    }
    public void setDogMood(String dogMood){
        this.dogMood=dogMood;
    }
    public String getDogMood(){
        return dogMood;
    }

}
