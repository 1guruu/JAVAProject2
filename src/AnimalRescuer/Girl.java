package AnimalRescuer;

public class Girl {

    private String girlName;
    private String girlRace;
    private int ageInYear;
    private int money;
    private String girlOccupation;

    private void setGirlName(String girlName){
        this.girlName=girlName;
    }
    public String getGirlName(){
        return girlName;
    }
    public void setGirlRace(String girlRace){
        this.girlRace=girlRace;
    }
    public String getGirlRace(){
        return girlRace;
    }
    public void setAgeInYear(int ageInYear){
        this.ageInYear=ageInYear;
    }
    public int getAgeInYear(){
        return ageInYear;
    }
    public void setMoney(int money){
        this.money=money;
    }
    public int getMoney(){
        return money;
    }
    public void setGirlOccupation(String girlOccupation){
        this.girlOccupation=girlOccupation;
    }
    public String getGirlOccupation(){
        return girlOccupation;
    }



}
